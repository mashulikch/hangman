namespace Hangman;
using System;

/// <summary>
/// Отвечает за визуализацию виселицы в консоли
/// </summary>
public class HangmanASCII
{
    private static readonly string[] stages =
    {
        // 0 ошибок
        "     \n" +
        "     \n" +
        "     \n" +
        "     \n" +
        "     \n" +
        "=====",

        // 1 ошибка — стойка
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "=====",

        // 2 ошибки — верх перекладины
        " +---+\n" +
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "=====",

        // 3 ошибки — веревка
        " +---+\n" +
        " |   |\n" +
        "     |\n" +
        "     |\n" +
        "     |\n" +
        "=====",

        // 4 ошибки — голова
        " +---+\n" +
        " |   |\n" +
        " O   |\n" +
        "     |\n" +
        "     |\n" +
        "=====",

        // 5 ошибок — туловище
        " +---+\n" +
        " |   |\n" +
        " O   |\n" +
        " |   |\n" +
        "     |\n" +
        "=====",

        // 6 ошибок — руки
        " +---+\n" +
        " |   |\n" +
        " O   |\n" +
        "/|\\  |\n" +
        "     |\n" +
        "=====",

        // 7 ошибок — ноги (полный человечек)
        " +---+\n" +
        " |   |\n" +
        " O   |\n" +
        "/|\\  |\n" +
        "/ \\  |\n" +
        "====="
    };

    /// <summary>
    /// Печатает ASCII-артистическую виселицу по количеству ошибок
    /// </summary>
    public static void Print(int errors)
    {
        Console.WriteLine(stages[Math.Min(errors, stages.Length - 1)]);
    }
}